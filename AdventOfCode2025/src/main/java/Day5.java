    private static int partOne(String[] input1, String[] input2) {
        int rangeLen = input1.length;
        long[][] range = new long[rangeLen][2];
        for (int i = 0; i < rangeLen; i++) {
            String r = input1[i];
            String[] split = r.split("-");
            range[i][0] = Long.parseLong(split[0]);
            range[i][1] = Long.parseLong(split[1]);
        }

        Arrays.sort(range, (a, b) -> Long.compare(a[0], b[0]) != 0 ? Long.compare(b[0], a[0]) : Long.compare(a[1], b[1]));
        int res = 0;
        for (String s : input2) {
            long num = Long.parseLong(s);
            for (long[] l : range) {
                if (l[0] > num) continue;
                if (num < l[1]) {
                    res++;
                    break;
                }
            }
        }
        return res;
    }


