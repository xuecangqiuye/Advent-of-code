    public static int solutionForPartOne(String[] blocks) {
        int res = 0;


        List<int[]> keys = new ArrayList<>();
        List<int[]> locks = new ArrayList<>();

        for (String s : blocks) {
            Boolean isKey = null;
            String[] split = s.split("\\n");
            int[] arr = new int[5];
            for (int i = 0; i < split.length - 1; i++) {
                String string = split[i];
                if (Objects.isNull(isKey) && Objects.equals(string, "#####")) {
                    isKey = false;
                    continue;
                }
                if (Objects.isNull(isKey) && Objects.equals(string, ".....")) {
                    isKey = true;
                    continue;
                }
                for (int j = 0; j < string.toCharArray().length; j++) {
                    if (string.charAt(j) == '#') {
                        arr[j] += 1;
                    }
                }
            }
            if (isKey) {
                keys.add(arr);
            } else {
                locks.add(arr);
            }
        }

        for (int[] key : keys) {
            for (int[] lock : locks) {
                boolean isPaired = true;
                for (int i = 0; i < key.length; i++) {
                    if (key[i] + lock[i] >= 6) {
                        isPaired = false;
                        break;
                    }
                }
                if (isPaired) {
                    res++;
                }
            }
        }
        return res;
    }
