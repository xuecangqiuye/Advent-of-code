    public static int solutionForPart1(List<List<Integer>> inputLists) {
        int anwser = 0;

        for (List<Integer> inputList : inputLists) {

            int gap = Math.abs(inputList.get(0) - inputList.get(inputList.size() - 1));
            if (gap > 3 * (inputList.size() - 1)) {
                continue;
            }

            Set<Integer> set = new HashSet<>(inputList);
            if (set.size() != inputList.size()) {
                continue;
            }

            boolean good = check(inputList);
            if (!good) {
                continue;
            }
            anwser++;

        }
        return anwser;
    }

    public static int solutionForPart2(List<List<Integer>> inputLists) {
        int anwser = 0;

        for (List<Integer> inputList : inputLists) {


            Set<Integer> set = new HashSet<>(inputList);
            if (set.size() < inputList.size()-1) {
                continue;
            }

            boolean good = check(inputList);
            if (!good) {
                for (int i = 0; i < inputList.size(); i++) {
                    List<Integer> tempList = new ArrayList<>(inputList);
                    tempList.remove(i);
                    boolean check = check(tempList);
                    if (check) {
                        anwser++;
                        break;
                    }
                }
            } else {
                anwser++;
            }
        }
        return anwser;
    }

    public static boolean check(List<Integer> inputList) {
        boolean aesc = false;
        boolean desc = false;
        for (int i = 1; i < inputList.size(); i++) {
            Integer s = inputList.get(i - 1);
            Integer e = inputList.get(i);
            if (s > e) {
                desc = true;
            } else {
                aesc = true;
            }
            if (desc && aesc) {
                break;
            }
            int diff = Math.abs(s - e);
            if (diff < 1 || diff > 3) {
                break;
            }

            if (i == inputList.size() - 1) {
                return true;
            }
        }
        return false;
    }

